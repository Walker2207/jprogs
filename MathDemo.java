import static java.lang.System.*;
import static java.lang.Math.*;

class MathDemo
{
 public static void main(String...arg)throws ClassNotFoundException
 {
  out.println("Power Function         : "+pow(2,3));
  out.println("Random Value           : "+random()*100);
  out.println("Sin60                  : "+sin(60));
  out.println("Cos60                  : "+cos(60));
  out.println("Max number b/w (5,6)   : "+max(5,6));
  out.println("SquareRoot of (456)    : "+sqrt(456));
  out.println("Radians                : "+sin(PI/2));  
 }
 
}

   
